int	ft_getwordlen(char const *str, char c)
{
	int	i;
	int	result;

	i = 0;
	result = 0;
	while (*(str + i) == c)
		i++;
	while (*(str + i) != c && *(str + i) != '\0')
	{
		i++;
		result++;
	}
	return (result);
}
